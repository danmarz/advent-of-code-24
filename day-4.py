def count_x_pattern(grid, target1, target2):
    n = len(grid)
    m = len(grid[0])
    count = 0

    # Function to validate a center for the X pattern
    def is_x_pattern(x, y):
        # Ensure we're within bounds for the X pattern
        if x - 1 < 0 or x + 1 >= n or y - 1 < 0 or y + 1 >= m:
            return False

        # Get diagonal characters
        top_left = grid[x - 1][y - 1]
        top_right = grid[x - 1][y + 1]
        center = grid[x][y]
        bottom_left = grid[x + 1][y - 1]
        bottom_right = grid[x + 1][y + 1]

        # Check all combinations of diagonals forming an X pattern
        return (
            # Both diagonals are target1
            (
                top_left == target1[0]
                and center == target1[1]
                and bottom_right == target1[2]
                and top_right == target1[0]
                and bottom_left == target1[2]
            )
            or
            # Both diagonals are target2
            (
                top_left == target2[0]
                and center == target2[1]
                and bottom_right == target2[2]
                and top_right == target2[0]
                and bottom_left == target2[2]
            )
            or
            # Diagonal 1: target1, Diagonal 2: target2
            (
                top_left == target1[0]
                and center == target1[1]
                and bottom_right == target1[2]
                and top_right == target2[0]
                and bottom_left == target2[2]
            )
            or
            # Diagonal 1: target2, Diagonal 2: target1
            (
                top_left == target2[0]
                and center == target2[1]
                and bottom_right == target2[2]
                and top_right == target1[0]
                and bottom_left == target1[2]
            )
        )

    # Traverse the grid
    for x in range(1, n - 1):
        for y in range(1, m - 1):
            if is_x_pattern(x, y):
                count += 1

    return count


