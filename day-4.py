        #     if string[i : i + len(target)] == target:
        #         matches += 1
        # return matches

    # Horizontal and reverse-horizontal
    for row in grid:
        row_string = "".join(row)
        count += count_matches(row_string, word)
        count += count_matches(row_string, reverse_word)

    # Vertical and reverse-vertical
    for col in range(m):
        col_string = "".join(grid[row][col] for row in range(n))
        count += count_matches(col_string, word)
        count += count_matches(col_string, reverse_word)

    # Diagonal (top-left to bottom-right)
    for d in range(-n + 1, m):  # Diagonal offsets
        diag_string = []
        for i in range(n):
            j = i + d
            if 0 <= i < n and 0 <= j < m:
                diag_string.append(grid[i][j])
        diag_string = "".join(diag_string)
        count += count_matches(diag_string, word)
        count += count_matches(diag_string, reverse_word)

    # Diagonal (top-right to bottom-left
    for d in range(-n + 1, m):  # Anti-diagonal offsets
        anti_diag_string = []
        for i in range(n):
            j = m - 1 - i - d
            if 0 <= i < n and 0 <= j < m:
                anti_diag_string.append(grid[i][j])
        anti_diag_string = "".join(anti_diag_string)
        count += count_matches(anti_diag_string, word)
        count += count_matches(anti_diag_string, reverse_word)

    return count


