#### Part 2
def causes_loop_with_obstacle(start_grid, obstacle_pos):
    # Copy the grid and place the obstacle
    grid = [row[:] for row in start_grid]
    grid[obstacle_pos[0]][obstacle_pos[1]] = "#"

    # Find the starting position and direction
    for r in range(rows):
        for c in range(cols):
            if grid[r][c] == "^":
                position = (r, c)
                direction = "^"
                break

    # Track visited positions and directions
    visited_states = set()
    while True:
        r, c = position
        dr, dc = moves[direction]
        new_r, new_c = r + dr, c + dc

        # Check if we're leaving the grid
        if not (0 <= new_r < rows and 0 <= new_c < cols):
            return False

        # Check if we've encountered a loop
        state = (position, direction)
        if state in visited_states:
            return True
        visited_states.add(state)

        # Check if there's something directly in front
        if grid[new_r][new_c] == "#":
            # Turn right 90 degrees
            direction = directions[(directions.index(direction) + 1) % 4]
        else:
            # Move forward
            position = (new_r, new_c)


# Find all potential positions for obstacles
obstacle_positions = [
    (r, c) for r in range(rows) for c in range(cols) if grid[r][c] == "."
]

# Count positions that cause a loop
loop_count = sum(
    1 for pos in obstacle_positions if causes_loop_with_obstacle(grid, pos)
)

print(f"number of positions that would cause a loop: {loop_count}")
