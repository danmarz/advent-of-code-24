grid = [list(row) for row in data.split("\n")]
rows, cols = len(grid), len(grid[0])

# Define the initial position and direction of the object
direction = "^"
directions = ["^", ">", "v", "<"]  # Up, Right, Down, Left (in clockwise order)
moves = {"^": (-1, 0), ">": (0, 1), "v": (1, 0), "<": (0, -1)}

# Find the starting position of the object
for r in range(rows):
    for c in range(cols):
        if grid[r][c] == "^":
            position = (r, c)
            break

# Initialize a set to track distinct visited positions
visited_positions = set()
visited_positions.add(position)

# Simulate movement
while True:
    r, c = position
    dr, dc = moves[direction]
    new_r, new_c = r + dr, c + dc

    # Check if we're leaving the grid
    if not (0 <= new_r < rows and 0 <= new_c < cols):
        break

    # Check if there's something directly in front
    if grid[new_r][new_c] == "#":
        # Turn right 90 degrees
        direction = directions[(directions.index(direction) + 1) % 4]
    else:
        # Move forward and add the position to the visited set
        position = (new_r, new_c)
        visited_positions.add(position)

